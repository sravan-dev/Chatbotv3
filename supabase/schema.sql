create or replace function public.sync_profile_from_auth()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.email is not null then
    insert into public.profiles (id, email, full_name)
    values (
      new.id,
      new.email,
      coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'name')
    )
    on conflict (id) do update
      set email = excluded.email,
          full_name = coalesce(excluded.full_name, public.profiles.full_name),
          updated_at = now();
  end if;

  return new;
end;
$$;

drop trigger if exists sync_profile_from_auth on auth.users;
create trigger sync_profile_from_auth
after insert or update on auth.users
for each row execute function public.sync_profile_from_auth();

create or replace function public.is_dashboard_user()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid()
      and role in ('admin', 'agent')
  );
$$;

create or replace function public.handle_new_message()
returns trigger
language plpgsql
as $$
begin
  update public.conversations
  set last_message_at = new.created_at,
      last_message_preview = left(regexp_replace(new.body, '\s+', ' ', 'g'), 160),
      unread_by_admin = case
        when new.sender_type = 'visitor' then unread_by_admin + 1
        else unread_by_admin
      end,
      unread_by_visitor = case
        when new.sender_type = 'admin' then unread_by_visitor + 1
        else unread_by_visitor
      end,
      status = case
        when new.sender_type = 'visitor' and status = 'closed' then 'open'
        else status
      end,
      assigned_admin_id = case
        when new.sender_type = 'admin' then coalesce(assigned_admin_id, new.sender_id)
        else assigned_admin_id
      end,
      updated_at = now()
  where id = new.conversation_id;

  return new;
end;
$$;

