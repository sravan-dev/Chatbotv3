create table if not exists public.conversations (
  id uuid primary key default gen_random_uuid(),
  visitor_id uuid not null references public.visitors (id) on delete cascade,
  status text not null default 'open' check (status in ('open', 'pending', 'closed')),
  assigned_admin_id uuid references public.profiles (id) on delete set null,
  last_message_at timestamptz not null default now(),
  last_message_preview text,
  unread_by_admin integer not null default 0,
  unread_by_visitor integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.messages (
  id uuid primary key default gen_random_uuid(),
  conversation_id uuid not null references public.conversations (id) on delete cascade,
  sender_id uuid references auth.users (id) on delete set null,
  sender_type text not null check (sender_type in ('visitor', 'admin', 'system')),
  body text not null check (char_length(trim(body)) > 0),
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.visitor_events (
  id bigint generated by default as identity primary key,
  visitor_id uuid not null references public.visitors (id) on delete cascade,
  conversation_id uuid references public.conversations (id) on delete set null,
  event_type text not null,
  pathname text,
  current_url text,
  page_title text,
  referrer text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.widget_settings (
  id text primary key default 'default',
  position text not null default 'right' check (position in ('left', 'right')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

insert into public.widget_settings (id, position)
